cin>>n;
int i=1;
while(i<=n){
    int space=n-i;
    while(space){
        cout<<"  ";
        space--;
    }
    int j=1;
    while(j<=i){
        cout<<"*"<<"   ";
        j++;
    }
    cout<<endl;
    i++;
}
int k=1;
while(k<=n){
    int space1=1;
    while(space1<=k){
        cout<<"  ";
        space1++;
    }
    int star1=n-k;
    while(star1>=1){
       cout<<"*"<<"   ";
       star1--;
    }
    cout<<endl;
    k++;
}
